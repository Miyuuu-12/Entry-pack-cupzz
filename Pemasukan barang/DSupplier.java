    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        String idsupplier = IdSupplierTxt.getText();

        // Check if ID supplier is not empty
        if (idsupplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID supplier tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Connection conn = null;
        PreparedStatement pstmt = null;
        try {
            conn = (Connection) Config.configDB();
            // SQL to delete data from table
            String sql = "DELETE FROM supplier WHERE id_supplier = ?";

            // Create PreparedStatement
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, idsupplier);

            // Execute SQL command
            int rowsDeleted = pstmt.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                resetForm();
                loadData();
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error",
                JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close connection and statement
            try {
                if (pstmt != null)
                pstmt.close();
                if (conn != null)
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String idsupplier = IdSupplierTxt.getText();
        String nama_supplier = NamaSupplierTxt.getText();
        String alamat_supplier = AlamatSupplierTxt.getText();
        String no_telepon = NoTelepon.getText();

        // Periksa apakah input tidak kosong
        if (idsupplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID supplier tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (nama_supplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama supplier tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (alamat_supplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jenis supplier tidak boleh kosong", "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!isNumeric(no_telepon)) {
            JOptionPane.showMessageDialog(this, "No telepon harus dengan angka", "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        } else if (no_telepon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harga supplier tidak boleh kosong", "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection conn = null;
        PreparedStatement pstmt = null;
        try {
            conn = (Connection) Config.configDB();
            // SQL untuk memasukkan data ke tabel
            String sql = "INSERT INTO supplier (id_supplier, nama_supplier, alamat_supplier, no_telepon) VALUES (?, ?, ?, ?)";

            // Membuat PreparedStatement
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, idsupplier);
            pstmt.setString(2, nama_supplier);
            pstmt.setString(3, alamat_supplier);
            pstmt.setString(4, no_telepon);

            // Menjalankan perintah SQL
            int rowsInserted = pstmt.executeUpdate();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                loadData();
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error",
                JOptionPane.ERROR_MESSAGE);
        } finally {
            // Menutup koneksi dan statement
            try {
                if (pstmt != null)
                pstmt.close();
                if (conn != null)
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

