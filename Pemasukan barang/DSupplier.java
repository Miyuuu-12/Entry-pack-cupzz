    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String idsupplier = IdSupplierTxt.getText();
        String nama_supplier = NamaSupplierTxt.getText();
        String alamat_supplier = AlamatSupplierTxt.getText();
        String no_telepon = NoTelepon.getText();

        // Periksa apakah input tidak kosong
        if (idsupplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID supplier tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (nama_supplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama supplier tidak boleh kosong", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (alamat_supplier.isEmpty()) {
            JOptionPane.showMessageDialog(this, "alamat supplier tidak boleh kosong", "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        } else if (no_telepon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harga supplier tidak boleh kosong", "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        Connection conn = null;
        PreparedStatement pstmt = null;
        try {
            conn = (Connection) Config.configDB();
            // SQL untuk memperbarui data di tabel
            String sql = "UPDATE supplier SET nama_supplier = ?, alamat_supplier = ?, no_telepon = ? WHERE id_supplier = ?";

            // Membuat PreparedStatement
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, nama_supplier);
            pstmt.setString(2, alamat_supplier);
            pstmt.setString(3, no_telepon);
            pstmt.setString(4, idsupplier);

            // Menjalankan perintah SQL
            int rowsUpdated = pstmt.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                loadData();
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error",
                JOptionPane.ERROR_MESSAGE);
        } finally {
            // Menutup koneksi dan statement
            try {
                if (pstmt != null)
                pstmt.close();
                if (conn != null)
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_updateBtnActionPerformed

